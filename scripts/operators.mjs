// The most basic Math operations functions are store here, they take two inputs and return the answer.

const divider = (a, b) => {
	return a / b;
};

const multiplier = (a, b) => {
	return a * b;
};

const adder = (a, b) => {
	return a + b;
};

const subber = (a, b) => {
	return a - b;
};

export { adder, divider, multiplier, subber };
